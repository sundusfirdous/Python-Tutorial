# type(): type() function check the data type of a variable or value

# Basic data types
x = 10
y = 10.4
name = 'Mike'
is_valid = True

print(type(x))
print(type(y))
print(type(name))
print(type(is_valid))

# Advanced data types

my_list = [10, 20, 30, 40]
my_tuple = (10, 20, 30, 40)
my_set = {10, 20, 30, 40}
my_dict = {"name": 'Sundus', "marks": 78.4}

print(type(my_list))
print(type(my_tuple))
print(type(my_set))
print(type(my_dict))